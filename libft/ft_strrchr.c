char	*ft_strrchr(const char *str, int c)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	while (c >= 128)
		c -= 128;
	while (i != -1)
	{
		if (str[i] == c)
			return ((char *)str + i);
		i--;
	}
	return (0);
}
