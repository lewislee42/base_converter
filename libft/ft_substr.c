#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*r_str;

	if (!s)
		return (NULL);
	if (start > ft_strlen(s))
		return (ft_strdup(""));
	if (len > ft_strlen(s + start))
		len = ft_strlen(s + start);
	r_str = malloc((len + 1) * sizeof(char));
	if (!r_str)
		return (NULL);
	ft_strlcpy(r_str, s + start, len + 1);
	return (r_str);
}
